#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*result;
	t_list	*temp;
	void	*new;

	result = NULL;
	while (lst)
	{
		new = f(lst->content);
		temp = ft_lstnew(new);
		if (!temp)
		{
			del(new);
			ft_lstclear(&result, del);
			return (NULL);
		}
		ft_lstadd_back(&result, temp);
		lst = lst->next;
	}
	return (result);
}
